def square(x, y):
    "Draw square using path at (x, y)."
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

def offset(point):
    "Return offset of point in tiles."
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

def valid(point):
    "Return True if point is valid in tiles."
    index = offset(point)

    if ctiles[index] == 0:
        return False

    index = offset(point + 19)

    if ctiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0



def validapp():
    while TRUE:
        applex = randrange(-180,100,20)
        appley = randrange(-160,160,20)
        apple = vector(applex,appley)
        if valid(apple) == TRUE:
            apple = vector(applex,appley)
            break
        else:
            continue
        
    


def world():
    "Draw world using path."
    bgcolor('black')
    path.color('blue')

    for index in range(len(ctiles)):
        tile = ctiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)   

    
    path.up()
    path.goto(apple.x,apple.y)
    path.dot(10, 'white')


               

def move():
    "Move pacman and all ghosts."
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            options = [
                vector(10, 0),
                vector(-10, 0),
                vector(0, 10),
                vector(0, -10),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 50)

def change(x, y):
    "Change pacman aim if valid."
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

validapp()
