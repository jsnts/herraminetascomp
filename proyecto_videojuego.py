# Con esta funcion podemos crear y dibujar un cuadrado con el cual introduzcas dos coordenadas y se haga. 
def square(x, y):
    "Draw square using path at (x, y)."
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

#Esta funcion nos da que tan desviada esta la localizacion en x y y de nuestro vector de 1s y 0s que hicimos anteriormente. 

def offset(point):
    "Return offset of point in tiles."
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


#Esta funcion utiliza la anterior para poder averiguar si el punto esta en un 1 o un 0 y nos ayuda para crear objetos y el movimiento de los mismos. 
def valid(point):
    "Return True if point is valid in tiles."
    index = offset(point)

    if ctiles[index] == 0:
        return False

    index = offset(point + 19)

    if ctiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

