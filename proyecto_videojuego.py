# Esta funcion crea y dibuja todo el mapa y el background al igual que dibuja nuestra manzana conlos valores adquiridos de la valdiacion. 
def world():
    "Draw world using path."
    bgcolor('green')
    path.color('brown')

    for index in range(len(ctiles)):
        tile = ctiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)   

    
    path.up()
    path.goto(apple.x +10,apple.y+10)
    path.dot(10, 'white')              

# Esta funcion es basicamente el jeugo donde podemos programar los movimientos del pacman y de lños fantasmas al igual que las colisones entrellos mismos y entre el pacman y la manzana. 

def move():
    "Move pacman and all ghosts."
    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'orange')

    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            options = [
                vector(10, 0),
                vector(-10, 0),
                vector(0, 10),
                vector(0, -10),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'purple')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            print('Has perdido!!!')
            color('orange')
            up()
            goto(0,0)
            style = ('Courier', 30, 'bold')
            write('Has Perdido.', font=style, align='center')
            goto(0,-30)
            write('Intenta otra vez!!', font=style, align='center')
            hideturtle()
            done()
            return

    if abs(pacman - apple) < 20:
            print('Has ganado!!!!')
            color('orange')
            up()
            goto(0,0)
            style = ('Courier', 30, 'bold')
            write('Has Ganado!!!', font=style, align='center')
            hideturtle()
            done()
            return
    

    ontimer(move, 30)

#Esta funcion nos sirve para poder cambiar de direccion al pacman con dos valores de x y y reespectivamente. 
def change(x, y):
    "Change pacman aim if valid."
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

#Aqui decidimos crear una funcion que aplique todas las funcio0nes para poder hacer un loop mas grande que simule el pasaje de niveles. 
